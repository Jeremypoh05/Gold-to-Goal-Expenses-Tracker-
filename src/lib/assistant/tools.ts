/** Run one tool by name; the engine wraps this per tool_use block. */
export async function executeAssistantTool(
  userId: string,
  name: string,
  input: ToolInput,
  now: Date = new Date(),
): Promise<unknown> {
  switch (name) {
    case "find_expenses":
      return findExpenses(userId, input);
    case "find_recurring":
      return findRecurring(userId, input);
    case "get_financial_overview":
      return getFinancialOverview(userId, now);
    case "analyze_spending":
      return analyzeSpending(userId, input);
    case "project_savings":
      return projectSavings(userId, input, now);
    case "get_closed_months":
      return getClosedMonths(userId);
    case "get_preferences":
      return getPreferences(userId);
    case "find_bonuses":
      return findBonuses(userId, input);
    // WRITE tools (Slice 2) — return a proposal (no DB write); engine → confirm card.
    case "create_expense":
      return proposeCreateExpense(userId, input, now);
    case "update_expense":
      return proposeUpdateExpense(userId, input);
    case "delete_expense":
      return proposeDeleteExpense(userId, input);
    case "create_recurring":
      return proposeCreateRecurring(userId, input, now);
    case "edit_recurring":
      return proposeEditRecurring(userId, input, now);
    case "set_preference":
      return proposeSetPreference(input);
    case "set_month_status":
      return proposeSetMonthStatus(userId, input);
    // WRITE tools (Slice 2d — income management).
    case "set_savings_goal":
      return proposeSetSavingsGoal(userId, input);
    case "adjust_salary":
      return proposeAdjustSalary(userId, input, now);
    case "create_bonus":
      return proposeCreateBonus(userId, input, now);
    case "update_bonus":
      return proposeUpdateBonus(userId, input);
    case "delete_bonus":
      return proposeDeleteBonus(userId, input);
    default:
      return { error: `unknown tool: ${name}` };
  }
}
