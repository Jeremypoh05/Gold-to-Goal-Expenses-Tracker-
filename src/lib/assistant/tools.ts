type ToolInput = Record<string, unknown>;

async function findExpenses(userId: string, input: ToolInput) {
  const where: Record<string, unknown> = { userId };

  const from = parseDay(input.dateFrom);
  const to = parseDay(input.dateTo, true);
  if (from || to) {
    where.spentAt = { ...(from && { gte: from }), ...(to && { lte: to }) };
  }
  if (typeof input.category === "string" && ALL_CATEGORIES.includes(input.category as CategoryKey)) {
    where.category = input.category;
  }
  const min = toNum(input.minAmount);
  const max = toNum(input.maxAmount);
  if (min != null || max != null) {
    where.amount = { ...(min != null && { gte: min }), ...(max != null && { lte: max }) };
  }
  if (input.source === "manual" || input.source === "voice") where.source = input.source;
  if (input.recurringOnly === true) where.fixed = true;
  if (typeof input.query === "string" && input.query.trim()) {
    const q = input.query.trim();
    where.OR = [
      { note: { contains: q, mode: "insensitive" } },
      { tags: { has: q.toLowerCase() } },
    ];
  }

  const sort = input.sort === "amount_desc"
    ? { amount: "desc" as const }
    : input.sort === "date_asc"
      ? { spentAt: "asc" as const }
      : { spentAt: "desc" as const };
  const limit = Math.min(100, Math.max(1, Math.round(toNum(input.limit) ?? 40)));

  const [rows, agg] = await Promise.all([
    prisma.expense.findMany({
      where,
      orderBy: sort,
      take: limit,
      select: {
        id: true,
        spentAt: true,
        category: true,
        amount: true,
        currency: true,
        note: true,
        tags: true,
        fixed: true,
        fixedSourceId: true,
        source: true,
      },
    }),
    prisma.expense.aggregate({ where, _count: true, _sum: { amount: true } }),
  ]);

  return {
    matchTotal: { count: agg._count, amount: Number(agg._sum.amount ?? 0) },
    returned: rows.length,
    expenses: rows.map((r) => ({
      id: r.id,
      date: fmtDate(r.spentAt),
      category: r.category,
      amount: Number(r.amount),
      currency: r.currency,
      note: r.note,
      ...(r.tags.length > 0 && { tags: r.tags }),
      ...(r.fixed && { recurring: true, ruleId: r.fixedSourceId }),
      ...(r.source === "voice" && { source: "voice" }),
    })),
  };
}

async function findRecurring(userId: string, input: ToolInput) {
  const where: Record<string, unknown> = { userId };
  if (input.includeInactive !== true) where.active = true;
  if (typeof input.query === "string" && input.query.trim()) {
    const q = input.query.trim();
    where.OR = [
      { label: { contains: q, mode: "insensitive" } },
      { note: { contains: q, mode: "insensitive" } },
    ];
  }
  const rules = await prisma.fixedExpense.findMany({ where, orderBy: { createdAt: "asc" } });
  const counts = await prisma.expense.groupBy({
    by: ["fixedSourceId"],
    where: { userId, fixedSourceId: { in: rules.map((r) => r.id) } },
    _count: true,
  });
  const countById = new Map(counts.map((c) => [c.fixedSourceId, c._count]));

  return {
    rules: rules.map((r) => ({
      ruleId: r.id,
      label: r.label,
      ...(r.note && { note: r.note }),
      category: r.category,
      amount: Number(r.amount),
      currency: r.currency,
      dueDay: r.dueDay,
      activeFrom: fmtYM(r.startYear, r.startMonth),
      activeUntil: r.endYear != null && r.endMonth != null ? fmtYM(r.endYear, r.endMonth) : "ongoing",
      active: r.active,
      generatedExpenseCount: countById.get(r.id) ?? 0,
    })),
    note:
      "Each rule generates one ledger expense per month in its range. Changing a rule updates every affected month; changing one generated expense changes only that month.",
  };
}

async function getFinancialOverview(userId: string, now: Date) {
  const year = now.getFullYear();
  const month = now.getMonth() + 1;
  const monthStart = new Date(year, month - 1, 1);
  const nextMonthStart = new Date(year, month, 1);

  const [user, periods, sources, bonuses, monthAgg, fixedRules, closedCount] = await Promise.all([
    prisma.user.findUnique({ where: { id: userId } }),
    prisma.salaryPeriod.findMany({ where: { userId } }),
    prisma.incomeSource.findMany({ where: { userId } }),
    prisma.bonus.findMany({ where: { userId, year } }),
    prisma.expense.aggregate({
      _count: true,
      _sum: { amount: true },
      where: { userId, spentAt: { gte: monthStart, lt: nextMonthStart } },
    }),
    prisma.fixedExpense.findMany({ where: { userId, active: true } }),
    prisma.monthClose.count({ where: { userId } }),
  ]);
  if (!user) return { error: "user not found" };

  const active = activeSalaryForMonth(periods.map(toUiSalaryPeriod), year, month);
  const otherIncome = recurringMonthlyIncome(sources.map(toUiIncomeSource), year, month);
  const cmp = (aY: number, aM: number, bY: number, bM: number) => (aY !== bY ? aY - bY : aM - bM);
  const activeRules = fixedRules.filter((f) => {
    const started = cmp(year, month, f.startYear, f.startMonth) >= 0;
    const notEnded = f.endYear == null || f.endMonth == null || cmp(year, month, f.endYear, f.endMonth) <= 0;
    return started && notEnded;
  });
  const spent = Number(monthAgg._sum.amount ?? 0);
  const budget = Number(user.monthlyBudget);

  return {
    today: fmtDate(now),
    displayCurrency: user.currency,
    income: {
      monthlyTakeHomeSalary: active?.monthlySalary ?? 0,
      grossSalary: active?.grossSalary ?? 0,
      deductions: active?.deductions ?? 0,
      otherRecurringMonthlyIncome: otherIncome,
      totalMonthlyIncome: (active?.monthlySalary ?? 0) + otherIncome,
      payDay: user.payDay,
      payFrequency: user.payFrequency,
      bonusesThisYear: bonuses.map((b) => ({ bonusId: b.id, month: b.month, amount: Number(b.amount), label: b.label })),
    },
    savings: {
      goal: Number(user.savingsGoal),
      saved: Number(user.saved),
      remainingToGoal: Math.max(0, Number(user.savingsGoal) - Number(user.saved)),
    },
    thisMonth: {
      month: fmtYM(year, month),
      spentSoFar: spent,
      expenseCount: monthAgg._count,
      monthlyBudget: budget,
      budgetRemaining: budget > 0 ? budget - spent : null,
    },
    recurringCommitments: {
      activeRuleCount: activeRules.length,
      totalMonthlyAmount: activeRules.reduce((a, f) => a + Number(f.amount), 0),
    },
    closedMonthCount: closedCount,
  };
}

async function analyzeSpending(userId: string, input: ToolInput) {
  const from = parseDay(input.dateFrom);
  const to = parseDay(input.dateTo, true);
  if (!from || !to) return { error: "dateFrom and dateTo must be YYYY-MM-DD or YYYY-MM" };
  if (to.getTime() - from.getTime() > 3 * 366 * 24 * 3600 * 1000) {
    return { error: "range too large — max 3 years per call" };
  }
  const groupBy = input.groupBy === "month" || input.groupBy === "tag" ? input.groupBy : "category";

  const rows = await prisma.expense.findMany({
    where: { userId, spentAt: { gte: from, lte: to } },
    select: { spentAt: true, amount: true, category: true, tags: true, fixed: true },
  });

  const groups = new Map<string, { total: number; count: number; recurring: number }>();
  let total = 0;
  let recurringTotal = 0;
  for (const r of rows) {
    const amt = Number(r.amount);
    total += amt;
    if (r.fixed) recurringTotal += amt;
    const keys =
      groupBy === "category"
        ? [r.category as string]
        : groupBy === "month"
          ? [fmtYM(r.spentAt.getFullYear(), r.spentAt.getMonth() + 1)]
          : r.tags.length > 0
            ? r.tags
            : ["(untagged)"];
    for (const k of keys) {
      const g = groups.get(k) ?? { total: 0, count: 0, recurring: 0 };
      g.total += amt;
      g.count += 1;
      if (r.fixed) g.recurring += amt;
      groups.set(k, g);
    }
  }

  return {
    range: { from: fmtDate(from), to: fmtDate(to) },
    overall: { total, expenseCount: rows.length, recurringPortion: recurringTotal, discretionaryPortion: total - recurringTotal },
    groupedBy: groupBy,
    groups: [...groups.entries()]
      .sort((a, b) => b[1].total - a[1].total)
      .slice(0, 40)
      .map(([key, g]) => ({ [groupBy]: key, total: g.total, count: g.count, recurringPortion: g.recurring })),
    ...(groupBy === "tag" && { note: "an expense can carry several tags, so tag totals may overlap" }),
  };
}

async function projectSavings(userId: string, input: ToolInput, now: Date) {
  const user = await prisma.user.findUnique({ where: { id: userId } });
  if (!user) return { error: "user not found" };

  const year = now.getFullYear();
  const month = now.getMonth() + 1;
  const target = toNum(input.targetAmount) ?? Number(user.savingsGoal);
  const saved = Number(user.saved);

  const [periods, sources] = await Promise.all([
    prisma.salaryPeriod.findMany({ where: { userId } }),
    prisma.incomeSource.findMany({ where: { userId } }),
  ]);
  const salary = activeSalaryForMonth(periods.map(toUiSalaryPeriod), year, month)?.monthlySalary ?? 0;
  const otherIncome = recurringMonthlyIncome(sources.map(toUiIncomeSource), year, month);
  const monthlyIncome = salary + otherIncome;

  // Average spend over the last 3 FULL calendar months (falls back to whatever
  // history exists; a brand-new user gets the current month so far).
  const windowStart = new Date(year, month - 4, 1);
  const windowEnd = new Date(year, month - 1, 1);
  const past = await prisma.expense.findMany({
    where: { userId, spentAt: { gte: windowStart, lt: windowEnd } },
    select: { spentAt: true, amount: true, category: true },
  });
  let avgSpend: number;
  let windowLabel: string;
  let byCategory: { category: string; monthlyAverage: number }[] = [];
  if (past.length > 0) {
    const monthsSeen = new Set(past.map((r) => `${r.spentAt.getFullYear()}-${r.spentAt.getMonth()}`)).size;
    const div = Math.max(1, monthsSeen);
    avgSpend = past.reduce((a, r) => a + Number(r.amount), 0) / div;
    windowLabel = `average of last ${div} full month(s)`;
    const catTotals = new Map<string, number>();
    for (const r of past) catTotals.set(r.category, (catTotals.get(r.category) ?? 0) + Number(r.amount));
    byCategory = [...catTotals.entries()]
      .sort((a, b) => b[1] - a[1])
      .map(([category, t]) => ({ category, monthlyAverage: Math.round((t / div) * 100) / 100 }));
  } else {
    const cur = await prisma.expense.aggregate({
      _sum: { amount: true },
      where: { userId, spentAt: { gte: new Date(year, month - 1, 1) } },
    });
    avgSpend = Number(cur._sum.amount ?? 0);
    windowLabel = "current month so far (no full-month history yet)";
  }

  const assumedSpend = toNum(input.monthlySpendOverride) ?? avgSpend;
  const monthlyNet = monthlyIncome - assumedSpend;
  const remaining = Math.max(0, target - saved);
  const monthsToTarget = remaining === 0 ? 0 : monthlyNet > 0 ? Math.ceil(remaining / monthlyNet) : null;
  let estimatedArrival: string | null = null;
  if (monthsToTarget != null) {
    const d = new Date(year, month - 1 + monthsToTarget, 1);
    estimatedArrival = fmtYM(d.getFullYear(), d.getMonth() + 1);
  }

  return {
    target,
    saved,
    remainingToTarget: remaining,
    monthlyIncome: { salary, otherRecurring: otherIncome, total: monthlyIncome },
    monthlySpend: {
      basis: toNum(input.monthlySpendOverride) != null ? "override (what-if)" : windowLabel,
      amount: Math.round(assumedSpend * 100) / 100,
    },
    monthlyNetSavings: Math.round(monthlyNet * 100) / 100,
    monthsToTarget,
    estimatedArrival,
    ...(monthsToTarget == null && remaining > 0 && {
      note: "monthly spend meets or exceeds income at this rate — the target is unreachable without changes",
    }),
    recentSpendByCategory: byCategory,
  };
}

